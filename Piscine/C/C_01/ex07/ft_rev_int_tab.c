#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int c;
	int p;

	c = 0;
	while (c < (size / 2))
	{
		p = tab[c];
		tab[c] = tab[(size - 1) - c];
		tab[(size - 1) - c] = p;
		c++;
	}
}
